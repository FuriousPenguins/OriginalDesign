public class OriginalDesign extends PApplet {

int a=100;
int b;
int x = 100;
int y = 100;

public void setup() {
	size(200,200);
	background(255);
}

public void draw() {
	x -= 1;
	b += 1;
	ellipse(100,100,x,y);
	ellipse(100,100,a,b);
}
  static public void main(String[] passedArgs) {
    String[] appletArgs = new String[] { "OriginalDesign" };
    if (passedArgs != null) {
      PApplet.main(concat(appletArgs, passedArgs));
    } else {
      PApplet.main(appletArgs);
    }
  }
}
